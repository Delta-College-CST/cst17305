# This program demonstrates various for-loops

# Prints out the numbers 0,1,2,3,4
for x in range(5):
	print(x)

print("======")   # Separator

# Prints out 5, 10, 15, …, 100
for x in range(5, 105, 5):
	print(x)

print("======")   # Separator

# Prints out 3,4,5
for x in range(3, 6):
	print(x)

print("======")   # Separator

# Prints out 10, 9, 8, …, 1
for x in range(10, 0, -1):
	print(x)
